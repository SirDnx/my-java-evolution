package com.condicoes.switchcase;

public class SwitchCase {

    public static void main(String[] args) {

        char plano = 'T';

        switch(plano){
            case 'T':
                System.out.println("5GB Youtube!");

            case 'M':
                System.out.println("Whatsapp sem limite!");

            case 'B':
                System.out.println("100 minutos de ligação!");
        }

    }
}
