package classes.estudos;

public class ClasseTiposDados {

    //por padrão o Java considera números inteiros como int e decimais como double
    private int unidade = 12;
    private double carteira = 55.96;

    //Mas para deixar mais claro pra JVM o tipo de dado, pode-se adicionar um caractere que reafirma o tipo
    //após o valor em si. O caractere pode ser minúsculo ou maiúsculo.

    private double dinheiro = 105d;

    private float salario = 500f;

    private long salario1 = 150l;
}
