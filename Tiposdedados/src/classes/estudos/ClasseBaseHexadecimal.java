package classes.estudos;

public class ClasseBaseHexadecimal {

    //Se for declarado um valor que se inicie com 0 precedido de x (0x), estará se tratando da base hexadecimal(16). Essa base vai do
    //0 ao 9 e de A até a letra F. O caractere x pode ser maiúsculo (X) ou minúsculo (x).
    //O valor na base hexadecimal "0x9A" equivale a 154

    private int baseHexadecimal = 0x9a;

    public int getBaseHexadecimal() {
        return baseHexadecimal;
    }
}
